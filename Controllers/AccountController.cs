        //
        // GET: /Account/SendCode
        [AllowAnonymous]
        public async Task<ActionResult> SendCode(string returnUrl, bool rememberMe)
        {
            var userId = await SignInManager.GetVerifiedUserIdAsync();
            if (userId == default(int))
            {
                return View("Error");
            }
            var userFactors = await UserManager.GetValidTwoFactorProvidersAsync(userId);
            var factorOptions = userFactors.Select(purpose => new SelectListItem { Text = purpose, Value = purpose }).ToList();
            return View(new SendCodeViewModel { Providers = factorOptions, ReturnUrl = returnUrl, RememberMe = rememberMe });
        }

        //
        // POST: /Account/SendCode
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> SendCode(SendCodeViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View();
            }

            // Generate the token and send it
            if (!await SignInManager.SendTwoFactorCodeAsync(model.SelectedProvider))
            {
                return View("Error");
            }
            return RedirectToAction("VerifyCode", new { Provider = model.SelectedProvider, ReturnUrl = model.ReturnUrl, RememberMe = model.RememberMe });
        }
     
        //
        // GET: /Account/ExternalLoginCallback
        [AllowAnonymous]
        public async Task<ActionResult> ExternalLoginCallback(string returnUrl)
        {
            var loginInfo = await AuthenticationManager.GetExternalLoginInfoAsync();
            
            if (loginInfo == null)
            {
                return RedirectToAction("Login");
            }
            
            // Sign in the user with this external login provider if the user already has a login
            var result = await SignInManager.ExternalSignInAsync(loginInfo, isPersistent: false);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = false });
                case SignInStatus.Failure:
                default:
                    // If the user does not have an account, then prompt the user to create an account
                    ViewBag.ReturnUrl = returnUrl;
                    ViewBag.LoginProvider = loginInfo.Login.LoginProvider;

                    AppUser user = GetExternalUserInfo(loginInfo);
                    
                    return View("ExternalLoginConfirmation", new ExternalLoginConfirmationViewModel { FirstName = user.FirstName, LastName = user.LastName, ProfilePicture = user.ProfilePicture, Email = loginInfo.Email });
            }
        }
        
        private AppUser GetFacebookInfo(ExternalLoginInfo loginInfo)
        {
            var accessToken = loginInfo.ExternalIdentity.Claims.FirstOrDefault(x => x.Type == "FacebookAccessToken").Value;
            var fbClient = new Facebook.FacebookClient(accessToken);
            var fbUser = fbClient.Get("/me?fields=name,first_name,last_name,picture,locale,age_range,gender").ToString();        
            dynamic fbInfo = JsonConvert.DeserializeObject(fbUser);

            Func<string, byte[]> getImage = imageUrl =>
            {
                byte[] img = null;
                using (WebClient client = new WebClient())
                {
                    using (Stream stream = client.OpenRead(imageUrl))
                    {
                        using (MemoryStream ms = new MemoryStream())
                        {
                            stream.CopyTo(ms);
                            img = ms.ToArray();
                        }                       
                    }
                }

                return img;
            };
            
            AppUser appUser = new AppUser()
            {
                FirstName = fbInfo.first_name ?? "",
                LastName = fbInfo.last_name ?? "",
                ProfilePicture = getImage((string)fbInfo.picture.data.url)
            };

            return appUser;
        }

        private AppUser GetGoogleInfo(ExternalLoginInfo loginInfo)
        {
            var claims = loginInfo.ExternalIdentity.Claims;

            AppUser user = new AppUser();

            foreach (Claim claim in claims)
            {                
                string subject = claim.Subject.ToString();
                
                if (claim.Type == App_Helpers.AppValues.GoogleOAuth2.GIVENNAME)
                {
                    user.FirstName = claim.Value ?? String.Empty;
                }
                else if (claim.Type == App_Helpers.AppValues.GoogleOAuth2.SURNAME)
                {
                    user.LastName = claim.Value ?? String.Empty;
                }
                else if (claim.Type == App_Helpers.AppValues.GoogleOAuth2.EMAIL)
                {
                    user.Email = claim.Value ?? String.Empty;
                }
            }
            
            return user;
        }

        private AppUser GetExternalUserInfo(ExternalLoginInfo loginInfo)
        {
            if (loginInfo.Login.LoginProvider == "Facebook")
            {
                return GetFacebookInfo(loginInfo);
            }
            else if (loginInfo.Login.LoginProvider == "Google")
            {
                return GetGoogleInfo(loginInfo);
            }

            return null;
        }


        //
        // POST: /Account/ExternalLoginConfirmation
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ExternalLoginConfirmation(ExternalLoginConfirmationViewModel model, string returnUrl)
        {
            if (User.Identity.IsAuthenticated)
            {
                return RedirectToAction("Index", "Manage");
            }

            if (ModelState.IsValid)
            {
                // Get the information about the user from the external login provider
                var info = await AuthenticationManager.GetExternalLoginInfoAsync();
                if (info == null)
                {
                    return View("ExternalLoginFailure");
                }
                var user = new AppUser { UserName = model.Email, Email = model.Email, FirstName = model.FirstName, LastName = model.LastName, BirthDate = model.BirthDate.Value, ProfilePicture = model.ProfilePicture };

                var result = await UserManager.CreateAsync(user);
                if (result.Succeeded)
                {
                    result = await UserManager.AddLoginAsync(user.Id, info.Login);
                    
                    if (result.Succeeded)
                    {
                        result = UserManager.AddToRole(user.Id, AppValues.AppRoles.USER);
                        
                        if (result.Succeeded)
                        {
                            await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);
                            return RedirectToLocal(returnUrl);
                        }                        
                    }
                }
                AddErrors(result);

            }

            ViewBag.ReturnUrl = returnUrl;
            return View(model);
        }

